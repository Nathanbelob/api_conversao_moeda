    public function login(LoginRequest $request)
    {
        $validacao = $request->validated();

        if (!$token = auth()->attempt($validacao)) {
            return response()->json(['error' => 'Unauthorized'], 401);
        }


        return response()->json([
            'access_token' => $token,
            'token_type' => 'bearer',
            'expires_in' => auth()->factory()->getTTL() * 60
        ]);
    }
